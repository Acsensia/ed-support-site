                    <h2>Gimbal-type weaponry</h2>
                    Gimbal-type mount is recommended for beginners and bounty hunters, as it does not require much effort from the pilot to use, while the damage remains at a decent level. The main disadvantage of this type of mount is that the weapon on the gimbal hits the enemy the worse, the farther away from you he is, at the maximum distance the effectiveness is reduced to zero. This is due to the fact that the barrel of the weapon with this type of mount when firing, although it is automatically aimed, but at the same time a little “ride” from side to side, the farther the target, the more this effect will be noticeable, and the less often the gimbal will hit. The easiest way to observe this effect is to use beam lasers - the direct beam will not hit the target exactly in one place, but will “ride” on it back and forth.
