                    <h1>Hybrid fighters</h1>
                    Hybrid fighters combine the best of standard and Guardian technology, making them more effective against targoids than conventional fighters. Targoid Interceptors, for example, have a damage resistance of 99% to conventional weapons, while Targoid Scouts have a damage resistance of 75%. Guardian weapons, including hybrid fighters, already do full damage.
                    <br><br>
                    These ships are light, fast, and maneuverable, with strong shields that allow them to rush into battle. The trade-off, as with other light fighters, is that they have a weaker hull. Compared to conventional fighters, hybrid fighters are significantly less defensible because they don't have built-in external equipment like a heat sink catapult or a dipole deflector spreader. In such a situation, the player-pilot or NPC pilot has to rely on his piloting skills and the maneuverability of the fighter. The combat rank of the NPC pilot plays a significant role in the fighter's survivability: NPCs are more likely to use afterburner and fly more complex trajectories.
