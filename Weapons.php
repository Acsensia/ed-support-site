                    <h2>Fixed weaponry</h2>
                    Fixed weapons have the highest damage of any weapon type with this type of mount in the game. They require high flying skills and a maneuverable ship (not necessarily fast, the main thing is that he had time to “follow” the target), otherwise they become practically useless - not keeping up with the target, you either will not do damage at all, or you will, but from time to time, when the enemy makes a mistake and allows you to shoot at yourself. They have the most minimal automatic aiming (no more than two degrees of deviation from the sight), but this minimal aiming is nevertheless extremely useful for accurate firing at long distances and for focusing certain modules on the ship, and as a consequence, fixed weapons - the most accurate and long-range in the game, can safely reach the enemy at a distance at which turrets or weapons on gimbal suspension will be practically useless.
