                    <h2>Kinetic damage</h2>
                    Kinetic damage is actually a physical characteristic of the kinetic energy imparted to the target by the kinetic energy imparted to the surface by the striking element when it is fired. As a consequence, ammunition is required for weapons that inflict this type of damage. In weightlessness, kinetic energy is not expended to cover the distance, as it is not subject to atmospheric resistance.
                    This type of damage is more effective against the target's armor and hull than against shields. There is a special armor that increases resistance to this type of damage - a composite with a reactive surface that dampens the kinetic energy of the projectile at the moment it touches the surface.
                </div>
