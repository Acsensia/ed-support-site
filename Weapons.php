                    <h2>Thermal damage</h2>
                    Thermal damage is actually the effect of spot heating of a surface to very high temperatures. Weapons that deal this type of damage usually generate a large amount of heat, heating both the weapon itself and the enemy surface.
                    Due to the high melting point of the materials from which ship armor (hull) is made, thermal damage is more effective against enemy shields than against armor (hull). There is a special armor that increases resistance to this type of damage - a composite with a mirror surface that reflects part of the heat ray into the surrounding space, thereby reducing the negative impact.
