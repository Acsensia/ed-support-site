                    <h2>Turret-type weaponry</h2>
                    Turret-type weapons are fully automated, with turrets capable of rotating up to 180° in either direction and target the enemy without any help from the pilot. Extremely unpredictable and peculiar weapons, possessing their own will and mind, sometimes so smart and shrewd that they “unmistakably” determine the enemy aboard “that ship from the Federation Navy in the wing with 6 more fighters”. The damage of these weapons is also very lame - as a rule, all types of turrets have the highest damage in their class and segment. For example, in the Class 1 Pulse Lasers segment, a turret mount will do the least damage. However, a Class 2 Pulse Turret, although it will be the weakest in its segment, will still be more powerful than the strongest Class 1. So don't relax and underestimate their damage - the 3rd class turrets are as powerful as any of the 2nd class turrets, and are self-pointing, even though they are weaker than other weapons of that size.
